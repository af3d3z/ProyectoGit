public class Conversacion {
	/***
	 * Saluda al mundo
	 * */
	public void saludar() {
		System.out.println("Hola Mundo!");
	}
	
	/***
	 * Saluda a alguien o algo en específico
	 * */
	public void saludar(String str) {
		System.out.println("Hola " + str + "!");
	}
}
